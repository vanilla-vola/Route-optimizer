def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    manifest_entries: list[dict] = []

    for entry in INSTANCES:
        id_suffix, city, region, pattern, description, stops_raw = entry
        doc = {
            "id": id_suffix,
            "description": description,
            "city": city,
            "region": region,
            "pattern": pattern,
            "mode": "driving-traffic",
            "round_trip": True,
            "stops": [
                {"name": name, "lat": lat, "lng": lng}
                for name, lat, lng in stops_raw
            ],
        }
        path = OUT / f"{id_suffix}.json"
        path.write_text(json.dumps(doc, indent=2) + "\n", encoding="utf-8")
        manifest_entries.append(
            {
                "id": id_suffix,
                "description": description,
                "city": city,
                "region": region,
                "pattern": pattern,
                "stop_count": len(stops_raw),
                "mode": doc["mode"],
                "round_trip": doc["round_trip"],
            }
        )

    catalog = {
        "version": 1,
        "count": len(manifest_entries),
        "note": "IEEE-oriented urban instances for Mapbox depart-at profile benchmarking.",
        "instances": manifest_entries,
    }
    (OUT / "manifest.json").write_text(
        json.dumps(catalog, indent=2) + "\n", encoding="utf-8"
    )
    full_instances = [
        json.loads((OUT / f"{entry['id']}.json").read_text(encoding="utf-8"))
        for entry in manifest_entries
    ]
    (ROOT / "benchmarks" / "instances_catalog.json").write_text(
        json.dumps(
            {
                "version": 1,
                "count": len(full_instances),
                "instances": full_instances,
            },
            indent=2,
        )
        + "\n",
        encoding="utf-8",
    )
    print(f"Wrote {len(manifest_entries)} instances to {OUT}")


if __name__ == "__main__":
    main()
